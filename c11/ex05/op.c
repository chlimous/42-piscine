#include "header.h"

int	ft_add(int nb1, int nb2)
{
	return (nb1 + nb2);
}

int	ft_sub(int nb1, int nb2)
{
	return (nb1 - nb2);
}

int	ft_multi(int nb1, int nb2)
{
	return (nb1 * nb2);
}

int	ft_div(int nb1, int nb2)
{
	return (nb1 / nb2);
}

int	ft_mod(int nb1, int nb2)
{
	return (nb1 % nb2);
}
